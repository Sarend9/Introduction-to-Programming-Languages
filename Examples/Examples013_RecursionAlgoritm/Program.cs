void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 2;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

